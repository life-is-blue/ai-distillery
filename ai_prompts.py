    "用户消息中无任何相关行为或表达\n\n"
    "合理推断 vs 过度放大（关键区分）：\n"
    "- 用户说'谋定而后动，先规划' → '决策模式：先规划再执行' = GROUNDED（直接证据）\n"
    "- 用户问'quality gate 固定方式有效吗' → '关注 quality gate 有效性' = GROUNDED（合理推断）\n"
    "- 用户问'quality gate 固定方式有效吗' → '明确反对补丁式质量门' = FABRICATED（语气放大）\n"
    "- 用户说'让 codex 执行，你负责验收' → '使用 codex 做执行分工' = GROUNDED\n"
    "- 用户说'让 codex 执行' → '坚持所有代码必须经 codex 审查' = FABRICATED（过度泛化）\n"
    "- 用户执行 git mv 删除 minimax- 前缀 → 推断用户拒绝 vendor 命名 = GROUNDED\n"
    "- 用户只做了 /clear → 推断用户'深耕IoT领域' = FABRICATED\n\n"
    "原则：观察的措辞强度不应超过用户原始表达的强度。\n"
    "如果观察方向正确但措辞过强，仍判 GROUNDED（上游 prompt 会调整措辞）。\n"
