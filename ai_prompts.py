    "- /clear、/resume、/compact 等会话管理命令是通用操作，"
    "不能作为技术偏好、工作节律或决策模式的证据\n"
    "## Structured field grounding rules:\n"
    "- Why: 字段需要用户消息中存在动机表达（'因为'/'为了'/'需要'/'不想'/'避免'），"
    "或用户行为可合理推断出动机（如拒绝某方案 → 推断不想要该方案的特性）\n"
    "- How: 字段是给 AI 的操作指令，不需要 grounding（跳过，不判断）\n"
    "- How: 字段长度 <15 字符 → 判 FABRICATED（过于模糊不可操作）\n"
    "- Evidence: 字段引用的用户原话或行为必须在原始消息中可找到\n\n"
